/*
  TRANSITION BETWEEN FOREST AND HOSPITAL - VIDEO
*/
import * as changeScene from '../changeScene.js';

export default class fth_video extends Phaser.Scene {
/*****************************************************************************************************************************************************/
/*****************************************************************************************************************************************************/
  constructor () {
    super('fth_video');
  }
/*****************************************************************************************************************************************************/
/*****************************************************************************************************************************************************/
  init (data) {
  }
/*****************************************************************************************************************************************************/
/*****************************************************************************************************************************************************/
  preload() {
  }
/*****************************************************************************************************************************************************/
/*****************************************************************************************************************************************************/
  create() {
    //Add change scene event listeners
    changeScene.addSceneEventListeners(this);

    this.tranVideo = this.add.video(640, 360, 'city');
    this.tranVideo.setDisplaySize(1280, 720);
    this.tranVideo.play();
  }
/*****************************************************************************************************************************************************/
/*****************************************************************************************************************************************************/
  update() {
    if (!this.tranVideo.isPlaying()) {
      this.scene.start('Hospital')
    }
  }
/*****************************************************************************************************************************************************/
/*****************************************************************************************************************************************************/
}
